    // Rule-based FAQ (keyword-response mapping)
    private Map<String, String> faqMap;

    public ChatBotUI() {
        // Window setup
        setTitle("ChatBot");
        setSize(500, 400);
        setDefaultCloseOperation(EXIT_ON_CLOSE);
        setLayout(null);
        setResizable(false);

        // Chat area (for conversation)
        chatArea = new JTextArea();
        chatArea.setEditable(false);
        chatArea.setLineWrap(true);
        chatArea.setWrapStyleWord(true);
        JScrollPane scrollPane = new JScrollPane(chatArea);
        scrollPane.setBounds(20, 20, 440, 250);
        add(scrollPane);

        // User input text field
        userInput = new JTextField();
        userInput.setBounds(20, 290, 340, 30);
        add(userInput);

        // Send button
        sendButton = new JButton("Send");
        sendButton.setBounds(370, 290, 90, 30);
        add(sendButton);

        // Load known FAQs
        initFAQ();

        // Send button logic
        sendButton.addActionListener(e -> handleUserInput());

        // Allow Enter key
        userInput.addActionListener(e -> handleUserInput());
    }

    private void initFAQ() {
        faqMap = new HashMap<>();
        faqMap.put("hello", "Hi! How can I help you?");
        faqMap.put("hi", "Hello there!");
        faqMap.put("how are you", "I'm just code, but thanks for asking!");
        faqMap.put("your name", "I'm JavaBot, your assistant.");
        faqMap.put("refund", "Refunds are handled in 3â€“5 business days.");
        faqMap.put("open", "We're open 24/7.");
        faqMap.put("bye", "Goodbye! Have a nice day.");
        faqMap.put("founder of pakistan", "Quaid-e-Aram Muhammad Ali Jinnah");
        faqMap.put("capital of pakistan", "Islamabad");
        faqMap.put("i am anum", "Hellow Anum! How can I help you?");
    }

    private void handleUserInput() {
        String input = userInput.getText().trim().toLowerCase();
        chatArea.append("You: " + input + "\n");

        String response = getResponse(input);
        chatArea.append("Bot: " + response + "\n\n");

        userInput.setText(""); // Clear the input field
    }

    private String getResponse(String input) {
        // Simple NLP: search keywords in user input
        for (String keyword : faqMap.keySet()) {
            if (input.contains(keyword)) {
                return faqMap.get(keyword);
            }
        }

        // Default response if keyword not found
        return "I'm not sure how to respond to that. Try asking something else.";
    }

